namespace Authoring.Core.Models;

/// <summary>
/// Represents the data type of a variable.
/// </summary>
public enum VariableType
{
    /// <summary>
    /// Boolean (true/false) variable type.
    /// </summary>
    Boolean,

    /// <summary>
    /// Numeric variable type.
    /// </summary>
    Number,

    /// <summary>
    /// Text string variable type.
    /// </summary>
    String
}
